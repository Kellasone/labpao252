package package1;

public class Masina {
    int numarUsi;
    String culoare;
    String distributie;

    public int getNumarUsi() {
        return numarUsi;
    }

    public void setNumarUsi(int numarUsi) {
        this.numarUsi = numarUsi;
    }

    public String getCuloare() {
        return culoare;
    }

    public void setCuloare(String culoare) {
        this.culoare = culoare;
    }

    public String getDistributie() {
        return distributie;
    }

    public void setDistributie(String distributie) {
        this.distributie = distributie;
    }
}
